def _make_config_with_skills(skills, zones=None):
    """创建测试用 Config"""
    default_zone = Zone(id="default", name="默认", priority=0, load_policy="free")
    all_zones = zones if zones else [default_zone]
    return Config(zones=all_zones, default_zone=default_zone, skills=skills, combos=[])


def test_build_generates_valid_agents_md(tmp_path):
    """build 输出文件存在且格式正确"""
    # 创建测试 skill 文件
    skills_dir = tmp_path / "skills"
    skills_dir.mkdir()
    
    skill_file = skills_dir / "test-skill.md"
    skill_file.write_text("# Test Skill\n\n测试内容", encoding="utf-8")
    
    # 创建配置
    skills = [
        SkillMeta(
            id="test-skill",
            name="测试技能",
            path=str(skill_file),
            summary="测试用技能",
            tags=["test"],
            load_policy="free",
            priority=100,
            zones=["default"],
            conflict_with=[],
        )
    ]
    
    config = _make_config_with_skills(skills)
    
    # 解析和生成
    resolver = Resolver(config)
    resolved = resolver.resolve()
    
    compressor = Compressor(resolved)
    manifest = compressor.compress()
    agents_md = compressor.generate_agents_md(manifest, resolved.active_zone)
    
    # 验证输出
    assert "## Required Skills" in agents_md
    assert "## Available Skills" in agents_md
    assert "Generated by Skills Orchestrator" in agents_md
    assert "测试技能" in agents_md
    assert "测试用技能" in agents_md


