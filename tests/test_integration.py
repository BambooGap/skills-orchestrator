def test_validate_catches_require_conflict():
    """validate 能检出 require 互冲突"""
    # 创建两个互相冲突的 require skill
    skill1 = SkillMeta(
        id="skill-a",
        name="技能A",
        path="/fake/skill-a.md",
        summary="技能A",
        tags=["a"],
        load_policy="require",
        priority=100,
        zones=["default"],
        conflict_with=["skill-b"],
    )
    
    skill2 = SkillMeta(
        id="skill-b",
        name="技能B",
        path="/fake/skill-b.md",
        summary="技能B",
        tags=["b"],
        load_policy="require",
        priority=100,
        zones=["default"],
        conflict_with=["skill-a"],
    )
    
    config = _make_config_with_skills([skill1, skill2])
    
    resolver = Resolver(config)
    
    # 应该抛出 ValueError
    with pytest.raises(ValueError, match="互相冲突"):
        resolver.resolve()


