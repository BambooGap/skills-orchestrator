def test_auto_discovery_from_dirs(tmp_path):
    """skill_dirs 自动发现：从 frontmatter 正确解析 skill 元数据"""
    # 创建子目录结构
    git_dir = tmp_path / "skills" / "git"
    git_dir.mkdir(parents=True)
    review_dir = tmp_path / "skills" / "review"
    review_dir.mkdir(parents=True)

    (git_dir / "my-git-skill.md").write_text(
        "---\n"
        "id: my-git-skill\n"
        "name: My Git Skill\n"
        "summary: git skill summary\n"
        "tags: [git]\n"
        "load_policy: free\n"
        "priority: 80\n"
        "zones: [default]\n"
        "conflict_with: []\n"
        "---\n\n# My Git Skill\n",
        encoding="utf-8",
    )
    (review_dir / "my-review-skill.md").write_text(
        "---\n"
        "id: my-review-skill\n"
        "name: My Review Skill\n"
        "summary: review skill summary\n"
        "tags: [review]\n"
        "load_policy: require\n"
        "priority: 150\n"
        "zones: [default]\n"
        "conflict_with: []\n"
        "---\n\n# My Review Skill\n",
        encoding="utf-8",
    )

    # 创建使用 skill_dirs 的 skills.yaml
    config_file = tmp_path / "skills.yaml"
    config_file.write_text(
        "version: '2.0'\n"
        "skill_dirs:\n"
        "  - ./skills\n"
        "zones:\n"
        "  - id: default\n"
        "    name: 默认区\n"
        "    load_policy: free\n"
        "    priority: 0\n"
        "    rules: []\n"
        "overrides: []\n"
        "combos: []\n",
        encoding="utf-8",
    )

    parser = Parser(str(config_file))
    config = parser.parse()

    assert len(config.skills) == 2
    ids = {s.id for s in config.skills}
    assert "my-git-skill" in ids
    assert "my-review-skill" in ids

    review = next(s for s in config.skills if s.id == "my-review-skill")
    assert review.load_policy == "require"
    assert review.priority == 150
    assert "review" in review.tags


def test_auto_discovery_overrides(tmp_path):
    """overrides 段正确覆盖 frontmatter 里的字段"""
    skills_dir = tmp_path / "skills"
    skills_dir.mkdir()

    (skills_dir / "some-skill.md").write_text(
        "---\n"
        "id: some-skill\n"
        "name: Some Skill\n"
        "summary: original summary\n"
        "tags: [tag1]\n"
        "load_policy: free\n"
        "priority: 50\n"
        "zones: [default]\n"
        "conflict_with: []\n"
        "---\n\n# Some Skill\n",
        encoding="utf-8",
    )

    config_file = tmp_path / "skills.yaml"
    config_file.write_text(
        "version: '2.0'\n"
        "skill_dirs:\n"
        "  - ./skills\n"
        "zones:\n"
        "  - id: default\n"
        "    name: 默认区\n"
        "    load_policy: free\n"
        "    priority: 0\n"
        "    rules: []\n"
        "overrides:\n"
        "  - id: some-skill\n"
        "    load_policy: require\n"
        "    priority: 200\n"
        "combos: []\n",
        encoding="utf-8",
    )

    parser = Parser(str(config_file))
    config = parser.parse()

    skill = config.skills[0]
    assert skill.id == "some-skill"
    # overrides 覆盖了 frontmatter
    assert skill.load_policy == "require"
    assert skill.priority == 200
    # 未覆盖的字段保持 frontmatter 原值
    assert skill.summary == "original summary"
    assert "tag1" in skill.tags


def test_auto_discovery_no_frontmatter(tmp_path):
    """无 frontmatter 的 .md 文件：从文件名推断 id，从内容推断 summary"""
    skills_dir = tmp_path / "skills"
    skills_dir.mkdir()

    (skills_dir / "plain-skill.md").write_text(
        "# Plain Skill Title\n\nThis is the skill description paragraph.\n",
        encoding="utf-8",
    )

    config_file = tmp_path / "skills.yaml"
    config_file.write_text(
        "version: '2.0'\n"
        "skill_dirs:\n  - ./skills\n"
        "zones:\n  - id: default\n    name: 默认区\n    load_policy: free\n    priority: 0\n    rules: []\n"
        "overrides: []\ncombos: []\n",
        encoding="utf-8",
    )

    parser = Parser(str(config_file))
    config = parser.parse()

    assert len(config.skills) == 1
    skill = config.skills[0]
    assert skill.id == "plain-skill"          # 从文件名推断
    assert skill.load_policy == "free"        # 默认值


